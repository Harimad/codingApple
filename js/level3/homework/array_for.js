var 출석부 = ['흥민', '영희', '철수', '재석'];

function 이름찾기(구멍){
  // 여기에 어떤 코드가 들어가야할까요?\
	출석부.forEach((elem) => {
		if (elem == 구멍) {
			console.log(`${구멍} 찾음`);
		}
	})
}

