function 목표량계산기(판매실적어레이, 평균월별판매량) {
	var 총판매해야할량 = 평균월별판매량 * 6;
	var 실적5개월 = 0;
	판매실적어레이.forEach((elem) => {
		실적5개월 += elem;
	})
	console.log(`12월엔 ${총판매해야할량 - 실적5개월}개를 팔아야합니다`);
}

목표량계산기( [10, 20, 30, 40, 50] , 35 );
목표량계산기( [11, 6, 2, 3, 5] , 10 );
